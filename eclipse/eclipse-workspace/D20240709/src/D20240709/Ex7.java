package D20240709;

public class Ex7 {

	public static void main(String[] args) {
		//증가감연산자
		
		//증가연산자
		int result, i;
		i = 1;
		result = i + 1;
		System.out.println(result);
		result = result + 1;
		System.out.println(result);
		result = result + 1;
		System.out.println(result);
		result ++; //result = result + 1; 증가연산자
		System.out.println(result);
		result ++;
		result ++;
		result ++;
		System.out.println(result);
		++result; //result = result + 1;
		System.out.println(result);
		
		//감소연산자
		result--;
		System.out.println(result);
		--result;
		System.out.println(result);
	}

}
